<template>
  <v-app class="siteManagementApplication">
    <v-main class="white mx-8 my-8">
      <site-management-sites-list :sites="sites"  class="mt-7"/>
    </v-main>
  </v-app>
</template>

<script>
export default {
  data() {
    return {
      sites: [],
    };
  },
  created() {
    this.getSites();
  },
  methods: {
    getSites() {
      this.loading = true;
      return this.$siteManagementService.getSites()
        .then(sites => {
          this.sites = sites || [];
        })
        .finally(() => this.loading = false);
    }
  }
};
</script>
